package com.example.myapplication

class Calculadora {

    fun somar(n1:Int, n2:Int): Int{
        val soma = n1 + n2
        return(soma)
    }
    fun multiplicar(n1:Int, n2:Int): Int{
        val multi = n1 * n2
        return(multi)
    }
    fun divisao(n1:Int, n2:Int): Int{
        val div = n1 / n2
        return(div)
    }
    fun subtrair(n1:Int, n2:Int): Int{
        val sub = n1 - n2
        return(sub)
    }

}